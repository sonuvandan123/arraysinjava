package switchcase;

public class SwitchCaseExample {

	public static void main(String[] args)
	{
		int dayVal=3;
		
		switch(dayVal)
		{
		      case 1:
		    	  System.out.println("Monday");
		    		    	  
		    	  break;
		    	  
		    	 
		      case 2:
		       
		    	  
		      case 3: 
		    	  System.out.println("Wednesday");
		         break;
		      default:
		    	  System.out.println("Not a day");	  
		    	  
		}

	}

}
